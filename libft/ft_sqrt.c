int	ft_sqrt(int num)
{
	int	i;

	if (num < 0)
		return (0);
	if (num == 0 || num == 1)
		return (num);
	i = 1;
	while (i * i <= num)
	{
		if (i * i == num)
			return (i);
		i++;
	}
	return (i);
}
